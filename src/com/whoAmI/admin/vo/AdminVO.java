package com.whoAmI.admin.vo;

public class AdminVO {
	private int managerNumber;
	private String managerName;
	private String managerId;
	private String managerPw;
	private int managerStatus;
	
	public AdminVO(){;}

	public int getManagerNumber() {
		return managerNumber;
	}

	public void setManagerNumber(int managerNumber) {
		this.managerNumber = managerNumber;
	}

	public String getManagerName() {
		return managerName;
	}

	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}

	public String getManagerId() {
		return managerId;
	}

	public void setManagerId(String managerId) {
		this.managerId = managerId;
	}

	public String getManagerPw() {
		return managerPw;
	}

	public void setManagerPw(String managerPw) {
		this.managerPw = managerPw;
	}

	public int getManagerStatus() {
		return managerStatus;
	}

	public void setManagerStatus(int managerStatus) {
		this.managerStatus = managerStatus;
	}
	
	@Override
	public String toString() {
		String str="managerNumber:"+managerNumber+"\n";
		str+="managerName:"+managerName+"\n";
		str+="managerId:"+managerId+"\n";
		str+="managerPw:"+managerPw+"\n";
		str+="managerStatus:"+managerStatus;
		return str;
	}
	
}


