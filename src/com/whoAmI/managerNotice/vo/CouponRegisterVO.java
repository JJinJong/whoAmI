package com.whoAmI.managerNotice.vo;

public class CouponRegisterVO {
	private String couponRegister;
	private String couponUserName;
	private String couponUserMail;
	private String couponContent;
	
	CouponRegisterVO(){;}

	public String getCouponRegister() {
		return couponRegister;
	}

	public void setCouponRegister(String couponRegister) {
		this.couponRegister = couponRegister;
	}

	public String getCouponUserName() {
		return couponUserName;
	}

	public void setCouponUserName(String couponUserName) {
		this.couponUserName = couponUserName;
	}

	public String getCouponUserMail() {
		return couponUserMail;
	}

	public void setCouponUserMail(String couponUserMail) {
		this.couponUserMail = couponUserMail;
	}

	public String getCouponContent() {
		return couponContent;
	}

	public void setCouponContent(String couponContent) {
		this.couponContent = couponContent;
	}
	
	
}
