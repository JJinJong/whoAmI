package com.whoAmI.managerCustomerCounseling.vo;

public class consultPaymentDetailVO {
	private int consultPaymentDetailNumber;
	private String consultType;
	private String consultTimes;
	private String consultStatus;
	private int managerNumber;
	private int userNumber;
	
	consultPaymentDetailVO(){;}

	public int getConsultPaymentDetailNumber() {
		return consultPaymentDetailNumber;
	}

	public void setConsultPaymentDetailNumber(int consultPaymentDetailNumber) {
		this.consultPaymentDetailNumber = consultPaymentDetailNumber;
	}

	public String getConsultType() {
		return consultType;
	}

	public void setConsultType(String consultType) {
		this.consultType = consultType;
	}

	public String getConsultTimes() {
		return consultTimes;
	}

	public void setConsultTimes(String consultTimes) {
		this.consultTimes = consultTimes;
	}

	public String getConsultStatus() {
		return consultStatus;
	}

	public void setConsultStatus(String consultStatus) {
		this.consultStatus = consultStatus;
	}

	public int getManagerNumber() {
		return managerNumber;
	}

	public void setManagerNumber(int managerNumber) {
		this.managerNumber = managerNumber;
	}

	public int getUserNumber() {
		return userNumber;
	}

	public void setUserNumber(int userNumber) {
		this.userNumber = userNumber;
	}
	
	
}
